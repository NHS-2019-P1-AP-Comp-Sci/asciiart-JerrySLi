public class ASCIIArt {
	
	public static void main (String [] args) {
		pumpkinTop();
		pumpkinFace();
		pumpkinBase();
		spookyQuote();
	}
	
	public static void pumpkinTop() {
		System.out.println("              __");
		System.out.println("              \\ \\");
		System.out.println("       _______/  \\________");
		System.out.println("     _/                   \\_");
		
	}
	
	public static void pumpkinFace() {
		System.out.println("    /     __         __     \\");
		System.out.println("   /      \\_\\       /_/      \\");
		System.out.println("   |     __           __     |");
		System.out.println("   |     \\ \\_________/ /     |");
		System.out.println("   \\      \\___________/      /");
	}
	
	public static void pumpkinBase() {
		System.out.println("    \\_                     _/");
		System.out.println("      \\___________________/");
	}
	
	public static void spookyQuote() {
		System.out.println("");
		System.out.println("\"September, the spookiest time of the year.\" -Miles Davis");
	}
	
}